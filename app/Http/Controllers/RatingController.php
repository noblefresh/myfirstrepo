    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        // return $request;
        $saveRate = new rating;
        $saveRate->productid = $request->productid;
        $saveRate->star = $request->star;
        $saveRate->username = $request->username;
        $saveRate->message = $request->message;
        if($saveRate->save()){
            return json_encode([
                'status' => 'success',
                'message' => 'Thanks for rating'
            ]);
        }else{
            return json_encode([
                'status' => 'error',
                'message' => 'An error occured!'
            ]);
        }
    }

